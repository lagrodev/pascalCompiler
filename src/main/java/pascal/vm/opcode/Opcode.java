package pascal.vm.opcode;

/**
 * Набор инструкций стековой виртуальной машины для Pascal.
 *
 * Соглашения:
 *   PUSH_*   — кладут значение на стек
 *   POP      — снимают значение со стека
 *   LOAD/STORE — работа с локальными переменными фрейма
 *   OP_*     — арифметические/логические операции (берут 2 аргумента с вершины стека)
 *   CMP_*    — сравнения (результат: 1 или 0)
 *   JMP/JZ   — безусловный/условный переход
 *   CALL/RET — вызов функции и возврат
 *   SYS_*    — системные операции (ввод/вывод)
 */
public enum Opcode {
    // ── Стек ──────────────────────────────────────────────────────
    PUSH_INT,       // arg: int value  → кладёт int на стек
    PUSH_BOOL,      // arg: boolean    → кладёт boolean на стек
    PUSH_STRING,    // arg: String     → кладёт строку на стек
    POP,            // снять вершину стека

    // ── Переменные ────────────────────────────────────────────────
    LOAD,           // arg: String varName  → загружает переменную из текущего фрейма на стек
    STORE,          // arg: String varName  → снимает вершину стека и сохраняет в переменную

    // ── Массивы ───────────────────────────────────────────────────
    ARRAY_LOAD,     // arg: String arrName; стек: [index] → кладёт arr[index] на стек
    ARRAY_STORE,    // arg: String arrName; стек: [index, value] → arr[index] = value
    NEW_ARRAY,      // arg: String varName, int start, int end → создаёт массив и кладёт в фрейм

    // ── Арифметика ────────────────────────────────────────────────
    OP_ADD,         // стек: [a, b] → a + b
    OP_SUB,         // стек: [a, b] → a - b
    OP_MUL,         // стек: [a, b] → a * b
    OP_DIV,         // стек: [a, b] → a div b (целочисленное)
    OP_MOD,         // стек: [a, b] → a mod b
    OP_NEG,         // стек: [a]    → -a

    // ── Сравнение → boolean (1 или 0) ─────────────────────────────
    CMP_EQ,         // =
    CMP_NE,         // <>
    CMP_LT,         // <
    CMP_LE,         // <=
    CMP_GT,         // >
    CMP_GE,         // >=

    // ── Логика ────────────────────────────────────────────────────
    OP_AND,
    OP_OR,
    OP_NOT,

    // ── Переходы ──────────────────────────────────────────────────
    JMP,            // arg: int address → безусловный переход
    JZ,             // arg: int address → прыгнуть если вершина стека == 0 (false)
    JNZ,            // arg: int address → прыгнуть если вершина стека != 0 (true)

    // ── Функции ───────────────────────────────────────────────────
    CALL,           // arg: String funcName, int argsCount → вызвать процедуру/функцию
    RET,            // выйти из текущей функции
    RET_VAL,        // выйти из функции с возвращаемым значением (оно на стеке)

    // ── Ввод / вывод ──────────────────────────────────────────────
    SYS_WRITE,      // arg: int count  → снять count значений со стека и вывести
    SYS_WRITELN,    // как SYS_WRITE, но с переводом строки
    SYS_READ,       // arg: String varName → прочитать и сохранить в переменную

    // ── Дубликаты стека ───────────────────────────────────────────
    DUP,            // продублировать вершину стека

    // ── Специальные ───────────────────────────────────────────────
    HALT,           // завершить программу
    LABEL,          // псевдоинструкция для разметки меток (убирается при линковке)
    NOP,            // no-operation
}
